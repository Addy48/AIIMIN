// ─── Metric Card ─────────────────────────────────────────────────────────────
export function drawMetricCard(doc, label, value, x, y, width, options = {}) {
    const { trend = null, color = GOLD, bg = [252, 250, 240], height = 28 } = options;
    
    // Background
    setFill(doc, bg);
    setDraw(doc, [220, 215, 200]);
    doc.setLineWidth(0.3);
    doc.roundedRect(x, y, width, height, 3, 3, 'FD');
    
    // Left accent bar
    setFill(doc, color);
    doc.roundedRect(x, y, 2, height, 1, 1, 'F');
    
    // Label
    setFont(doc, 7.5, 'normal', GRAY);
    doc.text(label.toUpperCase(), x + 6, y + 8);
    
    // Value
    setFont(doc, 16, 'bold', DARK);
    doc.text(String(value), x + 6, y + 21);
    
    // Trend arrow
    if (trend !== null && !isNaN(trend)) {
        const up = trend > 0;
        const neutral = trend === 0;
        setFont(doc, 9, 'bold', neutral ? GRAY : (up ? GREEN : RED));
        const arrow = neutral ? '→' : (up ? '↑' : '↓');
        doc.text(`${arrow} ${Math.abs(trend)}`, x + width - 16, y + 21);
    }
}

// ─── Score Badge ─────────────────────────────────────────────────────────────
export function drawScoreBadge(doc, score, x, y, radius = 20, label = '') {
    const pct = Math.max(0, Math.min(100, Number(score) || 0));
    const color = pct >= 75 ? GREEN : pct >= 50 ? GOLD : pct >= 30 ? ORANGE : RED;
    
    // Outer circle
    setDraw(doc, LIGHT_GRAY);
    doc.setLineWidth(0.5);
    doc.circle(x, y, radius);
    
    // Score arc (simulate filled)
    setDraw(doc, color);
    doc.setLineWidth(3);
    // Draw a partial circle to represent score (simplified as filled circle)
    setFill(doc, [...color, 20]);
    doc.circle(x, y, radius - 1);
    
    // Score text
    setFont(doc, 16, 'bold', color);
    doc.text(String(pct), x, y + 2, { align: 'center' });
    setFont(doc, 7, 'normal', GRAY);
    if (label) doc.text(label, x, y + radius + 6, { align: 'center' });
}

