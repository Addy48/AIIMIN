// ─── Radar Chart ─────────────────────────────────────────────────────────────
export function drawRadarChart(doc, scores = {}, cx = 105, cy = 90) {
    const radius = 40;
    const axes = [
        { key: 'physical', label: 'Physical', angle: -90 },
        { key: 'cognitive', label: 'Cognitive', angle: -18 },
        { key: 'discipline', label: 'Discipline', angle: 54 },
        { key: 'financial', label: 'Financial', angle: 126 },
        { key: 'emotional', label: 'Emotional', angle: 198 },
    ];
    
    // Grid rings
    [0.25, 0.5, 0.75, 1.0].forEach((level, i) => {
        const r = radius * level;
        const points = axes.map(axis => {
            const rad = (axis.angle * Math.PI) / 180;
            return [cx + Math.cos(rad) * r, cy + Math.sin(rad) * r];
        });
        
        setDraw(doc, i === 3 ? [200, 200, 210] : [225, 225, 235]);
        doc.setLineWidth(0.2);
        const path = points.map((pt, idx) => {
            const prev = points[idx === 0 ? points.length - 1 : idx - 1];
            return [pt[0] - prev[0], pt[1] - prev[1]];
        });
        doc.lines(path, points[points.length - 1][0], points[points.length - 1][1]);
    });
    
    // Score labels on rings
    setFont(doc, 6, 'normal', [180, 180, 190]);
    ['25', '50', '75', '100'].forEach((lbl, i) => {
        const r = radius * (i + 1) / 4;
        const rad = (-90 * Math.PI) / 180;
        doc.text(lbl, cx + Math.cos(rad) * r - 2, cy + Math.sin(rad) * r);
    });
    
    // Axis lines + labels
    axes.forEach(axis => {
        const rad = (axis.angle * Math.PI) / 180;
        const x = cx + Math.cos(rad) * radius;
        const y = cy + Math.sin(rad) * radius;
        
        setDraw(doc, [200, 200, 210]);
        doc.setLineWidth(0.2);
        doc.line(cx, cy, x, y);
        
        const lx = cx + Math.cos(rad) * (radius + 10);
        const ly = cy + Math.sin(rad) * (radius + 10);
        const score = Math.round(Number(scores[axis.key]) || 0);
        
        setFont(doc, 7.5, 'bold', DARK);
        doc.text(axis.label, lx, ly, { align: 'center' });
        setFont(doc, 6.5, 'normal', GRAY);
        doc.text(String(score), lx, ly + 4, { align: 'center' });
    });
    
    // Data polygon
    const polygon = axes.map(axis => {
        const score = Math.max(0, Math.min(100, Number(scores[axis.key] || 0)));
        const scaledRadius = radius * (score / 100);
        const rad = (axis.angle * Math.PI) / 180;
        return [cx + Math.cos(rad) * scaledRadius, cy + Math.sin(rad) * scaledRadius];
    });
    
    setDraw(doc, GOLD);
    doc.setLineWidth(1.5);
    setFill(doc, [245, 221, 137]);
    doc.setFillColor(212, 175, 55, 0.25);
    const path = polygon.map((pt, idx) => {
        const prev = polygon[idx === 0 ? polygon.length - 1 : idx - 1];
        return [pt[0] - prev[0], pt[1] - prev[1]];
    });
    doc.lines(path, polygon[polygon.length - 1][0], polygon[polygon.length - 1][1], [1, 1], 'FD', true);
    
    // Data point dots
    polygon.forEach(([px, py]) => {
        setFill(doc, GOLD);
        doc.circle(px, py, 1.5, 'F');
    });
}

// ─── Bar Chart ───────────────────────────────────────────────────────────────
export function drawBarChart(doc, data = [], options = {}) {
    const {
        x = 14, y = 44,
        width = 182, barHeight = 8,
        gap = 4,
        maxValue = 100,
        showLabels = true,
        showValues = true,
        color = GOLD,
        bgColor = LIGHT_GRAY,
    } = options;
    
    data.forEach((item, i) => {
        const rowY = y + i * (barHeight + gap + (showLabels ? 5 : 0));
        const pct = Math.max(0, Math.min(1, Number(item.value || 0) / maxValue));
        const barWidth = width * pct;
        
        if (showLabels) {
            setFont(doc, 7.5, 'normal', DARK);
            doc.text(item.label, x, rowY);
        }
        
        const barY = rowY + (showLabels ? 2 : 0);
        
        // Background bar
        setFill(doc, bgColor);
        doc.roundedRect(x, barY, width, barHeight, 1, 1, 'F');
        
        // Fill bar
        if (barWidth > 0.5) {
            setFill(doc, item.color || color);
            doc.roundedRect(x, barY, barWidth, barHeight, 1, 1, 'F');
        }
        
        // Value label
        if (showValues) {
            setFont(doc, 7, 'bold', item.color || color);
            doc.text(String(item.value), x + width + 3, barY + 6);
        }
    });
}

// ─── Trend Graph ─────────────────────────────────────────────────────────────
export function drawTrendGraph(doc, forecast = {}) {
    const chartX = 14, chartY = 50;
    const width = 182, height = 60;
    
    const series = [
        { key: 'lhs', label: 'Life Health Score', color: GOLD, value: forecast?.lhs?.projectedChange || 0 },
        { key: 'focus_cycles', label: 'Focus Cycles', color: BLUE, value: forecast?.focus_cycles?.projectedChange || 0 },
        { key: 'mood', label: 'Mood Score', color: PURPLE, value: forecast?.mood?.projectedChange || 0 },
    ];
    
    const maxMagnitude = Math.max(5, ...series.map(s => Math.abs(s.value)));
    
    // Chart background
    setFill(doc, [248, 248, 252]);
    setDraw(doc, LIGHT_GRAY);
    doc.setLineWidth(0.2);
    doc.roundedRect(chartX, chartY, width, height, 3, 3, 'FD');
    
    // Zero line
    setDraw(doc, [200, 200, 210]);
    doc.setLineWidth(0.3);
    doc.line(chartX + 40, chartY + height / 2, chartX + width - 5, chartY + height / 2);
    
    setFont(doc, 6, 'normal', GRAY);
    doc.text('0', chartX + 35, chartY + height / 2 + 2);
    
    // Series
    series.forEach((s, i) => {
        const rowY = chartY + 12 + i * 15;
        const normalized = (s.value / maxMagnitude) * (height / 2 - 8);
        const startX = chartX + 42;
        const midX = chartX + width / 2;
        const endX = chartX + width - 5;
        
        // Line from center to end
        setDraw(doc, s.color);
        doc.setLineWidth(1.5);
        doc.line(midX, rowY, endX, rowY - normalized);
        
        // Dot at end
        setFill(doc, s.color);
        doc.circle(endX, rowY - normalized, 1.5, 'F');
        
        // Label
        setFont(doc, 7, 'normal', DARK);
        doc.text(s.label, chartX + 5, rowY + 1.5);
        
        // Value
        const val = s.value;
        setFont(doc, 7, 'bold', s.color);
        doc.text(`${val >= 0 ? '+' : ''}${Number(val).toFixed(1)}`, endX + 3, rowY - normalized + 2);
    });
}

// ─── Habit Grid ──────────────────────────────────────────────────────────────
export function drawHabitGrid(doc, habits = [], y = 50) {
    const cols = 7;
    const cellW = 6, cellH = 5;
    const startX = 14;
    const today = new Date();
    const days = Array.from({ length: 30 }, (_, i) => {
        const d = new Date(today);
        d.setDate(today.getDate() - 29 + i);
        return d.toLocaleDateString('en-CA');
    });
    
    // Day labels
    setFont(doc, 5, 'normal', GRAY);
    days.forEach((d, i) => {
        const date = new Date(d);
        if (date.getDay() === 1 || i === 0) {
            doc.text(date.getDate().toString(), startX + 60 + i * (cellW + 1), y - 2);
        }
    });
    
    habits.slice(0, 8).forEach((habit, hi) => {
        const rowY = y + hi * (cellH + 3);
        const completedDates = habit.meta?.completedDates || [];
        const color = habit.meta?.color || '#3B82F6';
        const rgb = hexToRgb(color);
        
        // Habit name
        setFont(doc, 6, 'normal', DARK);
        doc.text(habit.name.substring(0, 10), startX, rowY + cellH - 1);
        
        // Grid cells
        days.forEach((d, di) => {
            const done = completedDates.includes(d);
            const cx = startX + 60 + di * (cellW + 1);
            
            if (done) {
                setFill(doc, rgb || GOLD);
            } else {
                setFill(doc, LIGHT_GRAY);
            }
            doc.roundedRect(cx, rowY, cellW, cellH, 0.5, 0.5, 'F');
        });
        
        // Completion %
        const pct = days.filter(d => completedDates.includes(d)).length;
        setFont(doc, 6, 'bold', rgb || GOLD);
        doc.text(`${pct}/30`, startX + 60 + days.length * (cellW + 1) + 3, rowY + cellH - 1);
    });
}

